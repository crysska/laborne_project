      <h2>Eglise La Borne : Historique de l'Eglise</h2>
      <img src="images/page3_img1.jpg" alt="" class="img_inner fleft">
      <div class="extra_wrapper">
        <p>
          L’initiative de former cette Eglise avait commencé à Macampagne, un des quartiers résidentiels de la capitale où habitait la famille Vernaud. Vers la deuxième moitié de 1981,
          parallèlement à ses campagnes d’évangélisation, il entreprit avec son épouse un ministère fort béni parmi les jeunes expatriés et inadaptés sociaux qui, après la délivrance,
          devaient être suivis pour leur croissance spirituelle. L’œuvre grandissait et se fit sentir le besoin de faire appel à certaines personnes converties, nées de nouveau, pour l’encadrement spirituel.
        </p>
        <p>
          Ainsi fut né le premier groupe d’hommes et de femmes composé des nationaux et d’expatriés. Parmi eux, on peut citer entre autres les familles suivantes : Ilunga Kabulu, Turner, Lines, Voorhoeve, Vilke,
          sœur Alphonsine, frère Aurelio Dacostas. Ce noyau s’était formé et affermi autour de trois couples missionnaires : J. Vernaud, W. Turner (américain) et Lines (français). Petit à petit, le groupe de base grandit.
          Les réunions de prières qui, au départ se tenaient dans la maison de la famille Vernaud, se déplaceront d’une famille à l’autre pendant l’absence de la famille Vernaud partie aux USA pour chercher les fonds nécessaires au
          lancement des projets d’une Eglise francophone.
        </p>
        <p>
          Le ministère pour les jeunes expatriés auquel s’était ajouté des jeunes africains assoiffés de la Parole de Dieu, était animé par Liliane, puis par Murielle après le départ de Liliane pour ses études, toutes deux filles du couple Vernaud.
          Un jeune américain, Charles Buller, fils d’un missionnaire Mennonite, y participa aussi durant une courte période de temps.
        </p>
        <p>
          Face aux multiples crises et aux mutations profondes et progressives que traverse la jeunesse de Kinshasa, le groupe né à Macampagne devient un des lieux actifs de reconstruction de l’identité des membres.
          Par l’action du Saint-Esprit, ces jeunes proposent des réponses aux aspirations des personnes blessées par la vie ou en situation de crise, tant au niveau du cœur que de l’intelligence. L’implication du couple Vernaud dans ce ministère fut totale.
        </p>
        <p>
          Aussi, le groupe devient-il un foyer d’épanouissement qui favorise une vraie vie communautaire où chaque fidèle se reconnaît et se sent aimé. Le souci de renforcer la solidarité entre les membres fait naître en chaque fidèle l’idée de la conscience collective.
          Pour les jeunes qui se retrouvent ainsi chaque semaine, la communauté est une réponse au besoin réel des chrétiens. Le succès de ce groupe ne va pas laisser dans l’indifférence les grandes personnes.
        </p>
        <p>
          En vue de concilier le message chrétien à la vie de la femme au foyer, les épouses Turner Sylvia et Ilunga Tshiala initièrent des rencontres mensuelles des femmes d’expression française. Le groupe ne tarda pas à s’accroître.
          Les épouses Vernaud, Voorheove et la sœur Alphonsine viendront grossir la communauté naissante. La place est donnée à la spontanéité, aux témoignages et à la participation de toutes. Les réunions de prières et d’études bibliques étaient orientées en
          fonction des besoins spécifiques des femmes ou des épouses.
          Les noyaux des jeunes, des hommes et des femmes ainsi formés s’affermissent de plus en plus.
        </p>
        </p>
          Vers la fin de l’année 1981, les choses se précipitèrent vers l’intégration de tous les groupes. Dès lors, il se pose le problème de trouver un lieu public de réunion. Le projet intéressera beaucoup de gens de bonne volonté et des promesses seront faites.
          C’est ainsi que le Pasteur J. Vernaud avec sa femme voyagera pour les Etats-Unis. Là, ils visiteront plus de deux cents églises pendant deux ans à la recherche de soutiens matériels et spirituels.Les fonds récoltés dans les Eglises gérés par le siège des Assemblées
          de Dieu des Etats-Unis, seront expédiés au Congo pour le besoin de l’achat d’une propriété.
        </p>
        </p>
          Dès le retour de la famille à Kinshasa en 1984, le Pasteur s’est mis à chercher un lieu où il pourrait construire le centre. Vers l’année 1963, lors d’une visite à Léopoldville et d’un court passage au restaurant de « La Borne » qui était à cette époque vide et sans activités, il dira à sa femme : « ça sera une place idéale pour faire une église »,
          paroles prononcées comme par « hasard », mais il n’y a pas de hasard avec Dieu.
        </p>
        </p>
          Eglise La Borne en 1992
          La vision était là quoique de façon inconsciente encore. Voilà que onze ans plus tard, il fut conduit auprès du responsable de ce restaurant par une fidèle. Le pasteur lui proposa de louer une partie du restaurant pour les réunions du dimanche tous les quinze jours du mois. Le propriétaire accepta la proposition mais refusa d’accepter de l‘argent. Désormais, les réunions se tiendront tous les dimanches. Plus tard, le pasteur eut l’idée d’acheter tout le complexe. Par la grâce du Seigneur, le marché fut conclu à la satisfaction de tous. Les églises des Assemblées de Dieu des Etats-Unis donnèrent suffisamment d’argent pour l’acquisition de la propriété de « La Borne ». L’agrandissement avec charpente métallique fut pour la moitié financé par les dons et offrandes des membres de l’église.
        </p>
